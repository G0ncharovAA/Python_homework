# 1. Напишите программу, которая принимает на вход цифру,
# обозначающую день недели, и проверяет, является ли этот день выходным.
#
# Пример:
#
# 6 -> да
# 7 -> да
# 1 -> нет
#
# Решение:

print('Введите цифру, обозначающую день недели:')
n = int(input())

if n in [6, 7]:
    print('Да')
# range - включает первое значение, но не включает последнее
elif n in range(1, 6):
    print('Нет')
else:
    print('Цифра вне диапазона дней недели')


# 2. Напишите программу для проверки истинности утверждения
# ¬(X ⋁ Y ⋁ Z) = ¬X ⋀ ¬Y ⋀ ¬Z для всех значений предикат.
#
# Решение:

def first_part(x, y, z):
    return not (x or y or z)


def second_part(x, y, z):
    return (not x) and (not y) and (not z)


def check(x, y, z):
    print(f"Для x={x}, y={y}, z={z}; выражение принимает значение: {first_part(x, y, z) == second_part(x, y, z)}")


for x in [True, False]:
    for y in [True, False]:
        for z in [True, False]:
            check(x, y, z)

