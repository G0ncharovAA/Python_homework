# 5. Напишите программу, которая принимает на вход координаты двух точек
# и находит расстояние между ними в 2D пространстве.
#
# Пример:
#
# - A (3,6); B (2,1) -> 5,09
# - A (7,-5); B (1,-1) -> 7,21
#
# Решение:

print('Введите координату Х точки А')
x_a = int(input())

print('Введите координату Y точки А')
y_a = int(input())

print('Введите координату Х точки B')
x_b = int(input())

print('Введите координату Y точки B')
y_b = int(input())

distance = sqrt(pow(x_a - x_b, 2) + pow(y_a - y_b, 2))

print(f"Расстояние между точками А и В составляет: {'{:.2f}'.format(distance)}")
